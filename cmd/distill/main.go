package main

import (
	"fmt"
	"os"

	"github.com/urfave/cli/v2"
)

func main() {
	app := &cli.App{
		Name:  "distill",
		Usage: "Distillation pipeline CLI tools",
		Commands: []*cli.Command{
			coverageCommand(),
			loadCommand(),
			exportCommand(),
		},
	}

	if err := app.Run(os.Args); err != nil {
		fmt.Fprintf(os.Stderr, "error: %v\n", err)
		os.Exit(1)
	}
}
